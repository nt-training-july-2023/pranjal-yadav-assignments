package Variables;

public class Increment {
    public static void main(String[] args) {
        int a=5;
        System.out.println(a);
        ++a;          // a becomes 6
        System.out.println(a);
        a++;          // a becomes 7
        System.out.println(a);
        --a;          // a becomes 6
        System.out.println(a);
        a--;          // a becomes 5
        System.out.println(a);


    }
}
