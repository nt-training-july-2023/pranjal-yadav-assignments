package Variables;

import javax.swing.plaf.synth.SynthScrollPaneUI;

public class q1 {
    public static void main(String[] args){
        //INTEGER
        int a=10;
        System.out.println("Value of a :"+a);
        a=2*a;
        System.out.println("Modified value of a: " +a);

        //FLOAT
        float b= 2.5f;
        System.out.println("Value of b :"+b);
        b=3*b;
        System.out.println("Modified value of b :"+b);

        //CHARACTER
        char c= 'a';
        System.out.println("Value of a :"+c);
        c='z';
        System.out.println("Modified value of a :"+c);

        //BOOLEAN
        boolean d= true;
        System.out.println("Value of d :"+d);
        d= false;
        System.out.println("Modified value of d :"+d);

        //SHORT
        short e= 3;
        System.out.println("Value of e :"+e);
        e= 4;
        System.out.println("Modified value of e :"+e);

        //BYTE
        byte f= 120;
        System.out.println("Value of f :"+f);
        f= 100;
        System.out.println("Modified value of f :"+f);

        //LONG
        long g= 200000;
        System.out.println("Value of g :"+g);
        g=8888888;
        System.out.println("Modified value of g :"+g);

        //DOUBLE
        double h= 5.4d;
        System.out.println("Value of h :"+h);
        h= h*2;
        System.out.println(" value of h :"+h);


    }


}
